package br.com.cesan.importacao.sgb.model.to;

public class ProfissionalSGBTO {
	
	private String tipoConselho;
	private String numeroConselho;
	private String ufConselho;
	private String nome;
	
	public String getTipoConselho() {
		return tipoConselho;
	}
	public void setTipoConselho(String tipoConselho) {
		this.tipoConselho = tipoConselho;
	}
	public String getNumeroConselho() {
		return numeroConselho;
	}
	public void setNumeroConselho(String numeroConselho) {
		this.numeroConselho = numeroConselho;
	}
	public String getUfConselho() {
		return ufConselho;
	}
	public void setUfConselho(String ufConselho) {
		this.ufConselho = ufConselho;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

}
