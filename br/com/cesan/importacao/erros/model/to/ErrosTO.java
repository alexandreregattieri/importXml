package br.com.cesan.importacao.erros.model.to;

public class ErrosTO {
	
	private String dataGuia;
	private String numeroGuia;
	private String matriculaGuia;
	private String nomeGuia;
	private String codigoErro;
	private String mensagemErro;
	private String cpfCnpj;
	private String nome;
	private String loteTiss;
	private String caminho;
	
	public String getDataGuia() {
		return dataGuia;
	}
	public void setDataGuia(String dataGuia) {
		this.dataGuia = dataGuia;
	}
	public String getNumeroGuia() {
		return numeroGuia;
	}
	public void setNumeroGuia(String numeroGuia) {
		this.numeroGuia = numeroGuia;
	}
	public String getMatriculaGuia() {
		return matriculaGuia;
	}
	public void setMatriculaGuia(String matriculaGuia) {
		this.matriculaGuia = matriculaGuia;
	}
	public String getNomeGuia() {
		return nomeGuia;
	}
	public void setNomeGuia(String nomeGuia) {
		this.nomeGuia = nomeGuia;
	}
	public String getCodigoErro() {
		return codigoErro;
	}
	public void setCodigoErro(String codigoErro) {
		this.codigoErro = codigoErro;
	}
	public String getMensagemErro() {
		return mensagemErro;
	}
	public void setMensagemErro(String mensagemErro) {
		this.mensagemErro = mensagemErro;
	}
	public String getCpfCnpj() {
		return cpfCnpj;
	}
	public void setCpfCnpj(String cpfCnpj) {
		this.cpfCnpj = cpfCnpj;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getLoteTiss() {
		return loteTiss;
	}
	public void setLoteTiss(String loteTiss) {
		this.loteTiss = loteTiss;
	}
	public String getCaminho() {
		return caminho;
	}
	public void setCaminho(String caminho) {
		this.caminho = caminho;
	}

}
