package br.com.cesan.importacao.xml.model.to.mensagem.corpo.guias.spsadt;

public class ValorTotalServicosTO {
	
	private Double servicosExecutados;
	private Double diarias;
	private Double taxas;
	private Double materiais;
	private Double medicamentos;
	private Double gases;
	private Double totalGeral;
	
	public Double getServicosExecutados() {
		return servicosExecutados;
	}
	public void setServicosExecutados(Double servicosExecutados) {
		this.servicosExecutados = servicosExecutados;
	}
	public Double getDiarias() {
		return diarias;
	}
	public void setDiarias(Double diarias) {
		this.diarias = diarias;
	}
	public Double getTaxas() {
		return taxas;
	}
	public void setTaxas(Double taxas) {
		this.taxas = taxas;
	}
	public Double getMateriais() {
		return materiais;
	}
	public void setMateriais(Double materiais) {
		this.materiais = materiais;
	}
	public Double getMedicamentos() {
		return medicamentos;
	}
	public void setMedicamentos(Double medicamentos) {
		this.medicamentos = medicamentos;
	}
	public Double getGases() {
		return gases;
	}
	public void setGases(Double gases) {
		this.gases = gases;
	}
	public Double getTotalGeral() {
		return totalGeral;
	}
	public void setTotalGeral(Double totalGeral) {
		this.totalGeral = totalGeral;
	}

}
