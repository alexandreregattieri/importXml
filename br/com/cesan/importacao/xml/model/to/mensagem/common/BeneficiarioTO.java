package br.com.cesan.importacao.xml.model.to.mensagem.common;

public class BeneficiarioTO {

	private String numeroCateira;
	private String nomeBeneficicario;
	private String nomePlano;
	private String validadeCarteira;
	private String numeroCNS;
	private String identificadorBeneficiario;
	
	public String getNumeroCateira() {
		return numeroCateira;
	}
	public void setNumeroCateira(String numeroCateira) {
		this.numeroCateira = numeroCateira;
	}
	public String getNomeBeneficicario() {
		return nomeBeneficicario;
	}
	public void setNomeBeneficicario(String nomeBeneficicario) {
		this.nomeBeneficicario = nomeBeneficicario;
	}
	public String getNomePlano() {
		return nomePlano;
	}
	public void setNomePlano(String nomePlano) {
		this.nomePlano = nomePlano;
	}
	public String getValidadeCarteira() {
		return validadeCarteira;
	}
	public void setValidadeCarteira(String validadeCarteira) {
		this.validadeCarteira = validadeCarteira;
	}
	public String getNumeroCNS() {
		return numeroCNS;
	}
	public void setNumeroCNS(String numeroCNS) {
		this.numeroCNS = numeroCNS;
	}
	public String getIdentificadorBeneficiario() {
		return identificadorBeneficiario;
	}
	public void setIdentificadorBeneficiario(String identificadorBeneficiario) {
		this.identificadorBeneficiario = identificadorBeneficiario;
	}
	
}
